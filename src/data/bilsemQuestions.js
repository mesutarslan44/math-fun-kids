export const getBilsemLevelById = (levelId) => {
    return BILSEM_LEVELS.find(level => level.id === levelId);
};

export const getRandomQuestion = (levelId) => {
    const level = getBilsemLevelById(levelId);
    if (!level) return null;
    const randomIndex = Math.floor(Math.random() * level.questions.length);
    return { ...level.questions[randomIndex], levelId };
};
